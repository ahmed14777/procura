// Helper function to get all selectable locations sorted by region and name
export function getSediOrdinatePerRegione(): SedeSelezionabile[] {
  return [...sediSelezionabili].sort((a, b) => {
    if (a.regione && b.regione) {
      const regionCompare = a.regione.localeCompare(b.regione, "it");
      if (regionCompare !== 0) return regionCompare;
    }
    return a.nome.localeCompare(b.nome, "it");
  });
}

// Helper function to find commission by ID
export function getCommissioneById(
  id: string,
): CommissioneTerritoriale | undefined {
  return commissioni.find((c) => c.id === id);
}

// Helper function to find sede by ID
export function getSedeById(id: string): SedeSelezionabile | undefined {
  return sediSelezionabili.find((s) => s.id === id);
}
